CREATE TABLE FILMES (ID INTEGER AUTO_INCREMENT NOT NULL, NOME VARCHAR(50) NOT NULL, GENERO VARCHAR(10) NOT NULL, PRECO DECIMAL NOT NULL, primary key (ID) );

CREATE TABLE CLIENTES (ID INTEGER AUTO_INCREMENT NOT NULL, NOME VARCHAR(50) NOT NULL, TELEFONE VARCHAR(10) NOT NULL, ENDERECO VARCHAR(50) NOT NULL, primary key (ID) );

CREATE TABLE LOCACOES (ID INTEGER AUTO_INCREMENT NOT NULL, DATA DATETIME NOT NULL, I_CLIENTES INTEGER NOT NULL, primary key (ID) );

CREATE TABLE ITENS_LOCACAO (ID INTEGER AUTO_INCREMENT NOT NULL, I_LOCACOES INTEGER NOT NULL, I_FILMES INTEGER NOT NULL, primary key (ID) );
